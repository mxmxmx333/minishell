#include "libft.h"

int	str_is_empty(char *str)
{
	while (*str)
	{
		if (*str != ' ' && *str != '\t' && *str != '\n' && *str != '\v' \
		&& *str != '\f' && *str != '\r')
			return (0);
		str++;
	}
	return (1);
}
